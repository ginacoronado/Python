#Vamos a organizarlo de manera diferente
df_computadores=df_computadores[duplicados_1].sort_values(['Company', 'Price_euros'])
df_computadores

df_computadores.value_counts('Company')

#Vamos ahora a manipular el parmetro keep que viene dentro de la funci칩n diplicated
#Por defecto este parametro trae el vlaor first
duplicados_firts=df_computadores.duplicated(['Company'], keep='first')

df_computadores[duplicados_firts]

#Para mostrar los no duplicaods de la columna campa침ia
df_computadores[~duplicados_firts]

#Voy a traer solo las columnas de mi interes
df_computadores[~duplicados_firts][['Company','Price_euros']]

#contamos los datos
df_computadores[~duplicados_firts].value_counts('Company')

#Para mostrar los valores finales de cada repetici칩n
diplicados_last=df_computadores.duplicated(['Company'], keep='last')

#Traifo el elemento final de repetidos
df_computadores[~diplicados_last]

#Para no confundirnos solo traeremos las tres columnas
df_computadores[~diplicados_last][['Company', 'Price_euros']]