df_computadores.duplicated(['Product', 'TypeName', 'Inches'])

#Guardo la condici칩n en una variable
duplicados_1=df_computadores.duplicated(['Product', 'TypeName', 'Inches'])

#Ahora filtro directamente en el dataframe
df_computadores[duplicados_1]

#Vamos a ordenar los datos con .sort segun lo que quiero ordenar
